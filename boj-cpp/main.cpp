#include <bits/stdc++.h>

using namespace std;
void fio() { cin.tie(nullptr); cout.tie(nullptr); ios::sync_with_stdio(false); }

int main() {
    fio();

    int t; cin >> t;
    while (t--) {
        int n, m; cin >> n >> m;

        for (int i = 0; i < m; ++i) {
            int u, v; cin >> u >> v;
        }

        cout << n - 1 << '\n';
    }
}
